/**
 * Writes docs/build-meta.js from package.json + current git HEAD (repo root).
 * Run via: pnpm exec node scripts/sync-static-build-meta.mjs
 * Wired into doc:sync and i18n:audit so static HTML pages show the same build line.
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { execFileSync } from 'node:child_process';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const pkg = JSON.parse(readFileSync(join(root, 'package.json'), 'utf8'));
const version = typeof pkg.version === 'string' ? pkg.version : '';

function gitLine(args) {
    try {
        const out = execFileSync('git', args, {
            cwd: root,
            encoding: 'utf8',
            stdio: ['ignore', 'pipe', 'ignore'],
        }).trim();
        return out || '';
    } catch {
        return '';
    }
}

const gitShaFull = gitLine(['rev-parse', 'HEAD']);
const gitShaShort =
    gitShaFull.length >= 7 ? gitShaFull.slice(0, 7) : gitShaFull || 'unknown';
const gitCommitDate = gitLine(['log', '-1', '--format=%cI']);

const obj = {
    version,
    gitShaShort: gitShaShort || 'unknown',
    gitShaFull: gitShaFull || 'unknown',
    gitCommitDate,
};

const js =
 '// Generated by scripts/sync-static-build-meta.mjs — do not edit by hand\n' +
    `window.__AUDIO_HAXOR_BUILD__ = ${JSON.stringify(obj)};\n`;

writeFileSync(join(root, 'docs', 'build-meta.js'), js, 'utf8');
console.log('Wrote docs/build-meta.js', obj);
